    <script>
        $(document).ready(function() {
            // Smooth scroll navbar
            $('a.nav-link').on('click', function(e) {
                if (this.hash !== "") {
                    e.preventDefault();
                    const hash = this.hash;
                    $('html, body').animate({
                        scrollTop: $(hash).offset().top - 100
                    }, 800);
                }
            });

            Fancybox.bind('[data-fancybox="gallery"]', {
                hideScrollbar: false,
                Images: { zoom: true }
            });

            // --- TECH TYPEWRITER LOGIC ---
            const textToType = "DOMINASI JALANAN.";
            const targetElement = document.getElementById("typed-text");
            let charIndex = 0;

            function typeEffect() {
                if (charIndex < textToType.length) {
                    let currentChar = textToType.charAt(charIndex);
                    if (charIndex >= 9) {
                        if (charIndex === 9) targetElement.innerHTML += '<span class="highlight">';
                        targetElement.querySelector('.highlight').innerHTML += currentChar;
                    } else {
                        targetElement.innerHTML += currentChar;
                    }
                    charIndex++;
                    setTimeout(typeEffect, 100);
                }
            }
            setTimeout(typeEffect, 2500);

            // API EMSIFA & MODE ANTI-ERROR (OFFLINE)
            const apiProvinsi = 'https://www.emsifa.com/api-wilayah-indonesia/api/provinces.json';
            const apiKabupaten = 'https://www.emsifa.com/api-wilayah-indonesia/api/regencies/';

            const fallbackProvinces = [
                { id: "34", name: "DI YOGYAKARTA" },
                { id: "31", name: "DKI JAKARTA" },
                { id: "32", name: "JAWA BARAT" },
                { id: "33", name: "JAWA TENGAH" },
                { id: "35", name: "JAWA TIMUR" },
                { id: "51", name: "BALI" }
            ];

            const fallbackRegencies = {
                "34": [{name: "KABUPATEN SLEMAN"}, {name: "KABUPATEN BANTUL"}, {name: "KOTA YOGYAKARTA"}],
                "31": [{name: "KOTA JAKARTA SELATAN"}, {name: "KOTA JAKARTA PUSAT"}],
                "33": [{name: "KOTA SEMARANG"}, {name: "KOTA SURAKARTA"}]
            };

            fetch(apiProvinsi)
                .then(response => response.json())
                .then(provinces => {
                    let options = '<option value="">-- Semua Provinsi --</option>';
                    provinces.forEach(prov => {
                        options += `<option value="${prov.name}" data-id="${prov.id}">${prov.name}</option>`;
                    });
                    $('#select-provinsi').html(options);
                })
                .catch(error => {
                    let options = '<option value="">-- Semua Provinsi (Mode Lokal) --</option>';
                    fallbackProvinces.forEach(prov => {
                        options += `<option value="${prov.name}" data-id="${prov.id}">${prov.name}</option>`;
                    });
                    $('#select-provinsi').html(options);
                });

            $('#select-provinsi').change(function() {
                const provId = $(this).find(':selected').data('id');
                const kabSelect = $('#select-kabupaten');

                if(provId) {
                    kabSelect.html('<option value="">Memuat Kabupaten...</option>').prop('disabled', true);
                    fetch(`${apiKabupaten}${provId}.json`)
                        .then(response => response.json())
                        .then(regencies => {
                            let options = '<option value="SEMUA">-- Semua Kabupaten/Kota --</option>';
                            regencies.forEach(reg => {
                                options += `<option value="${reg.name}">${reg.name}</option>`;
                            });
                            kabSelect.html(options).prop('disabled', false);
                        })
                        .catch(error => {
                            let options = '<option value="SEMUA">-- Semua Kabupaten/Kota --</option>';
                            if(fallbackRegencies[provId]) {
                                fallbackRegencies[provId].forEach(reg => {
                                    options += `<option value="${reg.name}">${reg.name}</option>`;
                                });
                            }
                            kabSelect.html(options).prop('disabled', false);
                        });
                } else {
                    kabSelect.html('<option value="">Pilih Provinsi Dahulu</option>').prop('disabled', true);
                }
            });

            // ==============================================================
            // AMBIL DATA DARI BACKEND MYSQL LARAVEL SECARA ASYNCHRONOUS
            // ==============================================================
            let databaseReklame = [];

            // Fetch data dari API saat halaman landing diload secara diam-diam (background)
            $.get('/api/balihos-new', function(data) {
                databaseReklame = data;
            });

            // --- VARIABEL PAGINATION FRONTEND ---
            let clientCurrentPage = 1;
            const clientRowsPerPage = 9;
            let currentFilteredData = [];

            $("#btn-cari").click(function() {
                const valProvinsi = $('#select-provinsi').val();
                const valKabupaten = $('#select-kabupaten').val();

                if(valProvinsi === "") {
                    alert("Silakan pilih provinsi terlebih dahulu.");
                    return;
                }

                $("#hasil-pencarian").fadeIn(400);

                let teksLokasi = valKabupaten !== "SEMUA" && valKabupaten !== "" ? `${valKabupaten}, ${valProvinsi}` : valProvinsi;
                $("#teks-hasil").html(`Titik Lokasi di <span class="text-danger">${teksLokasi}</span>`);

                // Filter dari databaseReklame yang didapat dari Backend
                let db = databaseReklame;

                currentFilteredData = db.filter(item => item.provinsi === valProvinsi);
                if(valKabupaten !== "" && valKabupaten !== "SEMUA") {
                    currentFilteredData = currentFilteredData.filter(item => item.kabupaten === valKabupaten);
                }

                clientCurrentPage = 1;
                renderClientGallery();

                $('html, body').animate({ scrollTop: $("#hasil-pencarian").offset().top - 120 }, 500);
            });

            window.changeClientPage = function(page) {
                clientCurrentPage = page;
                renderClientGallery();
                $('html, body').animate({ scrollTop: $("#hasil-pencarian").offset().top - 120 }, 300);
            };

            function renderClientGallery() {
                const container = $("#gallery-container").empty();
                const summaryContainer = $("#summary-container");
                const paginationBox = $("#client-pagination").empty();

                if(currentFilteredData.length === 0) {
                    container.append(`
                        <div class="col-12 text-center py-5">
                            <i class="fa-regular fa-folder-open mb-3" style="font-size: 4rem; color: #ced4da;"></i>
                            <h5 class="text-secondary fw-bold">Belum ada titik baliho di lokasi ini.</h5>
                        </div>
                    `);
                    summaryContainer.hide();
                    return;
                }

                const availCount = currentFilteredData.filter(i => i.status === "AVAILABLE").length;
                const soldCount = currentFilteredData.filter(i => i.status === "SOLD OUT").length;

                summaryContainer.html(`
                    <div class="result-summary">
                        <div class="summary-text"><i class="fa-solid fa-map-location-dot text-danger me-2"></i> Total ${currentFilteredData.length} Titik Ditemukan</div>
                        <div class="summary-badges">
                            <span class="badge-avail-summary"><i class="fa-solid fa-check-circle me-1"></i> ${availCount} Tersedia</span>
                            <span class="badge-sold-summary"><i class="fa-solid fa-ban me-1"></i> ${soldCount} Tersewa</span>
                        </div>
                    </div>
                `).show();

                const totalPages = Math.ceil(currentFilteredData.length / clientRowsPerPage);
                if(clientCurrentPage > totalPages) clientCurrentPage = totalPages;

                const startIndex = (clientCurrentPage - 1) * clientRowsPerPage;
                const endIndex = startIndex + clientRowsPerPage;
                const dataToDisplay = currentFilteredData.slice(startIndex, endIndex);

                dataToDisplay.forEach(data => {
                    const isSold = data.status === "SOLD OUT";
                    const badgeClass = isSold ? "badge-soldout" : "badge-available";
                    const imgClass = isSold ? "img-sold-out" : "";
                    const zoomBadge = `<span class='zoom-badge ${badgeClass}'>${data.status}</span>`;

                    container.append(`
                        <div class="col-lg-4 col-md-6 mb-4">
                            <div class="gallery-item shadow-sm">
                                <div class="status-badge ${badgeClass}">${data.status}</div>
                                <a href="${data.foto_besar}" data-fancybox="gallery" data-caption="${zoomBadge} ${data.titik} - ${data.kabupaten}" class="text-decoration-none">
                                    <div class="gallery-img-wrapper">
                                        <img src="${data.foto_kecil}" class="${imgClass}" alt="Baliho">
                                    </div>
                                    <div class="gallery-info">
                                        <h5>${data.titik}</h5>
                                        <p><i class="fa-solid fa-location-dot"></i> ${data.kabupaten}</p>
                                    </div>
                                </a>
                            </div>
                        </div>
                    `);
                });

                if(totalPages > 1) {
                    paginationBox.append(`<button class="page-btn" ${clientCurrentPage === 1 ? 'disabled' : ''} onclick="changeClientPage(${clientCurrentPage - 1})"><i class="fa-solid fa-chevron-left"></i></button>`);
                    for(let i = 1; i <= totalPages; i++) {
                        if(totalPages > 5) {
                            if(i === 1 || i === totalPages || (i >= clientCurrentPage - 1 && i <= clientCurrentPage + 1)) {
                                const active = i === clientCurrentPage ? 'active' : '';
                                paginationBox.append(`<button class="page-btn ${active}" onclick="changeClientPage(${i})">${i}</button>`);
                            } else if (i === clientCurrentPage - 2 || i === clientCurrentPage + 2) {
                                paginationBox.append(`<button class="page-btn" disabled>...</button>`);
                            }
                        } else {
                            const active = i === clientCurrentPage ? 'active' : '';
                            paginationBox.append(`<button class="page-btn ${active}" onclick="changeClientPage(${i})">${i}</button>`);
                        }
                    }
                    paginationBox.append(`<button class="page-btn" ${clientCurrentPage === totalPages ? 'disabled' : ''} onclick="changeClientPage(${clientCurrentPage + 1})"><i class="fa-solid fa-chevron-right"></i></button>`);
                }
            }

            // Jalankan animasi reveal setelah 2 detik
            setTimeout(function() {
                $('body').addClass('loaded');
            }, 2000);
        });
